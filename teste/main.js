function reverterString (string) {
    return string.split("").reverse().join("");
    // split("") -> cria um array separando por letras a string
    // reverse() -> inverte o array
    // join("") -> faz a junção de todos os elementos do array em uma string novamente
}

console.log(reverterString('teste'));
console.log(reverterString('testando'));


